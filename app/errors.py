"""系统错误码"""


class LogicError(Exception):
    """逻辑错误基类"""
    code = None


def gen_logic_error(name, code):
    """创建一个逻辑错误"""
    return type(name, (LogicError,), {'code': code})


# -----------------------------------------------------------------------------
# Main
# -----------------------------------------------------------------------------
# 服务器错误
IntervalServerError = gen_logic_error("Interval Server Error!", 500)
# 无效参数
InvalidParameter = gen_logic_error("Invalid Parameter", 2001)
# 更新失败
UpdateError = gen_logic_error("Update Error or Update data repeatedly", 2002)
# 验证失败
AuthError = gen_logic_error("Auth Error", 2003)

